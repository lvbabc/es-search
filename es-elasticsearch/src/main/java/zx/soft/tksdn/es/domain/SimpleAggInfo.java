package zx.soft.tksdn.es.domain;

/**
 * 简单的分片信息
 *
 * @author wanggang
 *
 */
public class SimpleAggInfo {

	private String name;
	private long value;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getValue() {
		return value;
	}

	public void setValue(long value) {
		this.value = value;
	}

}
